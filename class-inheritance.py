# what is inheritance ?

# when we class define a class that inheritance all properties of other class called inheritance

class father:
    def lands(self):
        print("50 eker ")

class son(father):
    def money(self):
        print("40 lacs")

s=son()
s.money()
s.lands()

# output :- 

# 40 lacs
# 50 eker