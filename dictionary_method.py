#                                          dictionary method 

# 1> keys
# 2> values
# 3> items
# 4> copy
# 5> clear
# 6> get 
# 6> pop 
# 7> update 

# example : dictionary 

a={'a':1,"b":3,'c':8}
print(a)

# output:-
# {'a': 1, 'b': 3, 'c': 8}

#-------------------------------------------------------------------------------------------------------------

# dictionary keys

print(a.keys())

# output:-
# dict_keys(['a', 'b', 'c'])

#---------------------------------------------------------------------------------------------------------

# dictionary values

print(a.values())

# output :-
# dict_values([1, 3, 8])

#----------------------------------------------------------------------------------------------------

# dictionary items

print(a.items()) 

# output:- 
# dict_items([('a', 1), ('b', 3), ('c', 8)])

#---------------------------------------------------------------------------------------------------
# dictionary copy

b=a.copy()
print(b)

# output:-
# {'a': 1, 'b': 3, 'c': 8}
#-------------------------------------------------------------------------------------------------------

# dictonary clear 

print(b.clear())

# output:-
# None

#----------------------------------------------------------------------------------------------------

# dictionary get value 

z=a.get("c")
print(z)

# output:-
# 8

x=a.get("a")
print(x)

# output:-
# 1

#---------------------------------------------------------------------------------------------------------------

# dictionary  remove specific element with help of pop 
z=a.pop("a")
print(a)

# output:-
# {'b': 3, 'c': 8}

#------------------------------------------------------------------------------------------------------------

# dictionary update

a.update({"d":10})
print(a)

